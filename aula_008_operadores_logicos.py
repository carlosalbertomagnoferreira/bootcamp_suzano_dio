# retornam um booleano
# operador and, retorna true se todas as condições forem true
# operador or, retorna true se uma das condições forem true
# Parentesis para organizar as condições

True and True # True
True and False # False
False and False # False

True or False # True
False or False # False
True or True # True

(True and False) or (True and True) # True
(True and True) and (True and False) # False

