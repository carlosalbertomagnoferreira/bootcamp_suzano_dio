# atribuicao simples '='
saldo = 100
# atribuicao com adição '+='
saldo += 100
# atribuicao com subtracao
saldo -= 100
# atribuicao com multiplicacao
saldo *= 2
# atribuicao com divisao
saldo /= 2
# atribuicao com divisao inteira
saldo //= 2
# atribuicao com modulo
saldo %= 3
#atribuicao com potenciacao
saldo **= 2

print(saldo)